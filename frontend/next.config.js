const path = require("path");
const fs = require("fs");

const generatedTsCandidates = [
  path.resolve(__dirname, "autogenerated/ts"),
  path.resolve(__dirname, "../autogenerated/ts"),
];
const generatedTsPath = generatedTsCandidates.find((candidate) =>
  fs.existsSync(path.join(candidate, "playground/spotify.ts")),
);

if (!generatedTsPath) {
  throw new Error(
    `Could not find generated TypeScript protobuf files in: ${generatedTsCandidates.join(", ")}`,
  );
}

const withBundleAnalyzer = require('@next/bundle-analyzer')({
  enabled: process.env.ANALYZE === 'true',
});

/** @type {import('next').NextConfig} */
const nextConfig = {
  // Emit a self-contained server bundle at .next/standalone/server.js, which is
  // what the Docker image and the NixOS service run in production.
  output: "standalone",

  reactStrictMode: true,
  swcMinify: true,

  // Optimize bundle size
  experimental: {
    optimizePackageImports: [
      'react-icons',
      '@react-three/drei',
      '@react-three/fiber',
      'react-syntax-highlighter',
      'three/examples/jsm',
      'three-stdlib',
      'react-markdown',
      'remark-gfm',
      'rehype-raw',
      'mermaid'
    ],
    turbo: {
      rules: {
        '*.svg': {
          loaders: ['@svgr/webpack'],
          as: '*.js',
        },
      },
    },
    // Enable modern bundling optimizations
    esmExternals: true,
    serverComponentsExternalPackages: ['prismjs'],
    webVitalsAttribution: ['CLS', 'LCP'],
  },

  transpilePackages: ['three', '@react-three/fiber', '@react-three/drei', 'troika-three-text', 'three-stdlib'],

  webpack: (config, { isServer }) => {
    config.resolve.alias = {
      ...config.resolve.alias,
      "@": path.resolve(__dirname, "src"),
      "@amirsalarsafaei/autogenerated": generatedTsPath,
    };

    // The generated protobuf TS lives outside this project root
    // (../autogenerated/ts) during local dev, so resolution walking up from
    // those files never reaches frontend/node_modules. Add it explicitly so
    // their runtime deps (@bufbuild/protobuf, grpc-web, …) resolve from here.
    //
    // It MUST be appended (a fallback), not prepended: prepending an absolute
    // path makes webpack resolve every bare import from the top-level
    // node_modules first, defeating nested per-package versions. That breaks
    // @react-three/drei (needs its nested zustand v5) by handing it the
    // top-level zustand v3 that @react-three/fiber pulls in — surfacing as
    // "zustand.create is not a function". Keeping the default relative
    // resolution first preserves nested resolution; the absolute entry only
    // catches the out-of-tree protobuf deps that relative resolution misses.
    config.resolve.modules = [
      ...(config.resolve.modules || ["node_modules"]),
      path.resolve(__dirname, "node_modules"),
    ];

    // Exclude server-only packages from client bundle
    if (!isServer) {
      const { IgnorePlugin } = require('webpack');
      config.plugins = config.plugins || [];
      config.plugins.push(
        new IgnorePlugin({
          resourceRegExp: /@improbable-eng\/grpc-web-node-http-transport/,
        })
      );
    }

    return config;
  },

  poweredByHeader: false,
  compress: true,

  eslint: {
    ignoreDuringBuilds: true,
  },

  typescript: {
    ignoreBuildErrors: true,
  },
  pageExtensions: ["tsx", "ts", "jsx", "js"],
  images: {
    remotePatterns: [
      {
        protocol: "http",
        hostname: "localhost",
        port: "3001",
        pathname: "/images/**",
      },
      {
        protocol: "https",
        hostname: "upload.amirsalarsafaei.com",
        pathname: "/images/**",
      },
    ],
    formats: ['image/webp', 'image/avif'],
    minimumCacheTTL: 60,
  },
};

module.exports = withBundleAnalyzer(nextConfig);
