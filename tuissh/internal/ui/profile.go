// barWidth is the number of segments in a skill proficiency bar.
const barWidth = 5

type profileLink struct {
	label string
	url   string
}

type skill struct {
	name  string
	level string // lowercase label, e.g. "expert"
	score int    // filled segments out of barWidth, derived from the enum
}

type resumeSection struct {
	heading string
	body    string
}

type resumeInfo struct {
	fullName  string
	email     string
	githubURL string
	sections  []resumeSection
}

type profileInfo struct {
	name         string
	title        string
	bio          string // markdown
	currentFocus string // markdown
	skills       []skill
	resume       resumeInfo
	links        []profileLink
	imageURL     string // profile photo, shown atop the about page
}

type profileLoadedMsg struct {
	profile profileInfo
	err     error
}

// levelLabel/levelScore map the Skill.Level enum to its display label and the
// number of filled bar segments. "passion" denotes enthusiasm rather than
// mastery, so it sits between learning and proficient.
func levelLabel(l profilepb.Skill_Level) string {
	switch l {
	case profilepb.Skill_EXPERT:
		return "expert"
	case profilepb.Skill_PROFICIENT:
		return "proficient"
	case profilepb.Skill_PASSION:
		return "passion"
	case profilepb.Skill_LEARNING:
		return "learning"
	default:
		return ""
	}
}

func levelScore(l profilepb.Skill_Level) int {
	switch l {
	case profilepb.Skill_EXPERT:
		return 5
	case profilepb.Skill_PROFICIENT:
		return 4
	case profilepb.Skill_PASSION:
		return 3
	case profilepb.Skill_LEARNING:
		return 2
	default:
		return 0
	}
}

func mkSkill(name string, l profilepb.Skill_Level) skill {
	return skill{name: name, level: levelLabel(l), score: levelScore(l)}
}

